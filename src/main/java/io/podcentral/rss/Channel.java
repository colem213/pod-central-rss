package io.podcentral.rss;

import java.util.Date;
import java.util.List;

import javax.xml.bind.annotation.XmlElement;

import org.eclipse.persistence.oxm.annotations.XmlPath;

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAttribute;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBIgnore;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBIndexHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;

import lombok.Data;

@DynamoDBTable(tableName = "Channel")
@Data
public class Channel {
  @DynamoDBHashKey(attributeName = "id")
  @DynamoDBAutoGeneratedKey
  private String id;
  @DynamoDBAttribute
  private String title;
  @DynamoDBAttribute
  private String description;
  @DynamoDBAttribute
  private String link;
  @DynamoDBIndexHashKey(globalSecondaryIndexName = "feedUrl")
  @DynamoDBAttribute
  @XmlPath("atom:link[@rel='self']/@href")
  private String feedUrl;
  @DynamoDBAttribute
  private Date pubDate;
  @DynamoDBAttribute
  private String language;
  @DynamoDBAttribute
  @XmlPath("itunes:image/@href")
  private String imageUrl;
  @DynamoDBAttribute
  @XmlPath("itunes:category/@text")
  private String category;
  @DynamoDBAttribute
  @XmlElement(name = "explicit", namespace = XmlNs.ITUNES)
  private Boolean explicit;
  @DynamoDBIgnore
  @XmlElement(name = "item")
  private List<Item> items;
}
